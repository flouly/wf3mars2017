// Creer une variable de type number avec la valeur 10

// Operateur d'affectation

var myNumber = 10;
console.log(myNumber);


// Operateur d'incrementation: ++

myNumber++;
console.log(myNumber);

// Operateur de decrementation : --

myNumber--;
console.log(myNumber);

// Operateur d'addition/contatenation: +

myAdd = 7 + 5;
console.log(myAdd);


var myConc  = 'Salut ' + 'mon ' + 'pote';
console.log(myConc);

// Operateur de soustraction

var mySous = 7 - 5;
console.log(mySous);

// Operateur de multiplication

var myMult = 7 * 5;
console.log(myMult);

// Operateur de division

var myDivi = 14 / 3;
console.log(myDivi);

// Operateur modulo: %
var myModulo = 17 % 2;
console.log(myModulo);

// Operateur d'addition et d'affectation: +=

var first = 10;
var second = 5;

// first = first + second;
first += second;
console.log(first);


// Operateur de soustraction et d'affectation

second -= first;
console.log(second);

